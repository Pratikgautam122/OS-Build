void register_interrupt_handler(unsigned char n, interrupt_handler_t handler) {
    interrupt_handlers[n] = handler;
}

void interrupt_handler(registers_t* regs) {
    // If it's an IRQ, send End of Interrupt (EOI) to PIC
    if (regs->int_no >= 32 && regs->int_no < 48) {
        pic_send_eoi(regs->int_no - 32);
    }

    if (interrupt_handlers[regs->int_no] != 0) {
        interrupt_handlers[regs->int_no](regs);
    } else {
        if (regs->int_no < 32) {
            vga_printf("PANIC: Unhandled Exception %d (error %x) at EIP %x!\n", 
                       regs->int_no, regs->err_code, regs->eip);
            // Halt the CPU
            for (;;) {
                __asm__ volatile("cli; hlt");
            }
        }
    }
}
